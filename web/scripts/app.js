    document.getElementById('stat-total').innerText = growthPoints;
    document.getElementById('stat-stage').innerText = `Phase ${stageIdx+1}`;
    document.getElementById('stat-next').innerText = stage.max === 999 ? '∞' : stage.max - growthPoints;
    

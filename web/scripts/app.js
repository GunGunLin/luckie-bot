function nextGuideBubble() {
    if (guideIdx >= guideQueue.length) return;
    const msg = guideQueue[guideIdx];
    // Highlight current step
    document.querySelectorAll('.guide-step').forEach((s, i) => {
        s.classList.remove('active-step');
        if (i < guideIdx) s.classList.add('done-step');
        if (i === guideIdx) s.classList.add('active-step');
    });
    showSpriteBubble(msg);
    guideIdx++;
    if (bubbleTimer) clearTimeout(bubbleTimer);
    bubbleTimer = setTimeout(nextGuideBubble, 4000);
}


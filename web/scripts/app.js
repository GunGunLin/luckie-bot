    const fill = document.getElementById('gp-fill');
    const progress = stage.max === 999 ? 100 : ((growthPoints - stage.min) / (stage.max - stage.min)) * 100;
    document.getElementById('gp-stage').innerText = stage.name;
    document.getElementById('gp-points').innerText = stage.max === 999 
        ? `${growthPoints} Divinations (Max Level)` 
        : `${growthPoints - stage.min} / ${stage.max - stage.min} required`;

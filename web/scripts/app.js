const fxCanvas = document.getElementById('magic-fx');
const fxCtx = fxCanvas.getContext('2d');
let particles = [];
class Particle {
    constructor(x, y, color) {
        this.x = x; this.y = y;
        this.vx = (Math.random()-0.5)*3; this.vy = (Math.random()-0.5)*3 - 1;
        this.size = Math.random()*3+1;
        this.life = 1.0;
        this.hue = color || `hsla(${Math.random()*20+40}, 80%, 60%, `;
        this.isGold = !color;
    }
    update() { this.x += this.vx; this.y += this.vy; this.vy -= 0.02; this.life -= 0.018; }
    draw() {
        fxCtx.fillStyle = this.isGold ? this.hue + this.life + ')' : this.hue;
        fxCtx.globalAlpha = this.life;
        fxCtx.shadowBlur = 8; fxCtx.shadowColor = 'gold';
        fxCtx.beginPath(); fxCtx.arc(this.x, this.y, this.size, 0, Math.PI*2); fxCtx.fill();
        fxCtx.globalAlpha = 1;
    }
}
function animateFX() {
    fxCtx.clearRect(0,0,fxCanvas.width,fxCanvas.height);
    particles = particles.filter(p => p.life > 0);
    particles.forEach(p => { p.update(); p.draw(); });
    requestAnimationFrame(animateFX);
}
window.addEventListener('resize', () => { fxCanvas.width = window.innerWidth; fxCanvas.height = window.innerHeight; });
fxCanvas.width = window.innerWidth; fxCanvas.height = window.innerHeight;
animateFX();


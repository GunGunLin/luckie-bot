function handleSpriteUpload(e) {
    const file = e.target.files[0];
    if (!file) return;
    const reader = new FileReader();
    reader.onload = (ev) => {
        customSpriteImg = ev.target.result;
        localStorage.setItem('lb_sprite_img', customSpriteImg);
        applyCustomSprite();
        showSpriteBubble("Wow, do I look better now? ✨");
    };
    reader.readAsDataURL(file);
}


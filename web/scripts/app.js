const introCanvas = document.getElementById('intro-canvas');
const introCtx = introCanvas.getContext('2d');
let introParticles = [];

function resizeIntroCanvas() {
    introCanvas.width = window.innerWidth;
    introCanvas.height = window.innerHeight;
}
window.addEventListener('resize', resizeIntroCanvas);
resizeIntroCanvas();

function initIntroParticles() {
    introParticles = [];
    for (let i = 0; i < 150; i++) {
        introParticles.push({
            x: introCanvas.width/2,
            y: introCanvas.height/2,
            vx: (Math.random()-0.5)*8,
            vy: (Math.random()-0.5)*8,
            alpha:1,
            size: Math.random()*3+1,
            color: `hsla(${Math.random()*60+30}, 80%, 60%, 1)`
        });
    }
}

function animateIntro() {
    introCtx.clearRect(0,0,introCanvas.width,introCanvas.height);
    introParticles.forEach(p=>{
        p.x+=p.vx; p.y+=p.vy; p.alpha-=0.01;
        introCtx.fillStyle = `hsla(${Math.random()*60+30}, 80%, 60%, ${p.alpha})`;
        introCtx.beginPath();
        introCtx.arc(p.x,p.y,p.size,0,Math.PI*2);
        introCtx.fill();
    });
    introParticles = introParticles.filter(p=>p.alpha>0);
    if(introParticles.length>0) requestAnimationFrame(animateIntro);
}

